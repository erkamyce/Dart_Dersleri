void main (){
  //Sabitler
  //final , const for dart
  //(final) for java & (let) for swift & (val) for kotlin
  int sayi = 30;
  print(sayi);
  sayi= 300;
  print(sayi);

  //programi calistirdigimizda hafizada yer olusturur
  final int deger = 300;
  print(deger);

  //degeri tanimladigimizda hafizada yer olusturur
  //gorsel nesnelerde kullanilir.Button,Text vb.
  const int reged = 300;
  print(reged);

  //final ve const sabit degiskenlerinde degiskenler bir kez tanimlarin ve ondan sonra deger deistirilmez
}